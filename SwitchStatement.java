
public class SwitchStatement {

	public static void main(String[] args) {
		char Grade = 'A';
		
		switch(Grade) {
		  case 'A':
		System.out.println("A");
		
		  case 'B':
		System.out.println("B");
		
		  case 'C':
		System.out.println("C");
		default:
			System.out.println("D");
		}
		System.out.println("Statement");

	}

}
