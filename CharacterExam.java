public class CharacterExam {
    public static void main(String[] a) {
        System.out.println(Character.isLetter('L'));
        System.out.println(Character.isLetter(5));
        System.out.println(Character.isLowerCase('m'));
        System.out.println(Character.isLowerCase('K'));
        System.out.println(Character.isUpperCase('u'));
        System.out.println(Character.toUpperCase('m'));
        System.out.println(Character.toLowerCase('P'));
        System.out.println(Character.toString('G'));
    }
}
